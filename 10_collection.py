# collection: single variable to store multiple values. Examples: list, tuple, set, dictionary

# List = [] : mutable -> can be modified, ordered, changeable, allows duplicate members 
# Tuple = () : immutable -> like strings can be modified, ordered, unchangeable, allows duplicate members 
# Set = {} : mutable -> can be modified, unordered, unindexed, no duplicate members


List = [4, 1, 2, 3]
print(List, type(List))    

Tuple = (1, 2, 3) 
print(Tuple, type(Tuple))

Set = {5, 1, 2, 3}
print(Set, type(Set))  








